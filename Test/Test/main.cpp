#include <PPLogger.hpp>

int main()
{
	// 测试1：默认增强彩色日志
	PPLOG_DEBUG("这是调试信息 - 深灰色");
	PPLOG_INFO("这是普通信息 - 青色");
	PPLOG_WARNING("这是警告信息 - 黄色");
	PPLOG_ERROR("这是错误信息 - 红色");
	PPLOG_FATAL("这是致命错误 - 洋红色");

	// 测试2：格式化输出
	PPLOG_INFO("用户ID: %d, 姓名: %s", 88888, "飘云");

	// 测试3：切换到复合日志（控制台+文件）
	auto compositeLogger = std::make_unique<CompositeLogger>();
	PP_LOGGER.SetLogger(std::move(compositeLogger));
	PP_LOGGER.Init("app.log");

	PPLOG_INFO("这条消息会同时出现在控制台和文件中");

	// 测试4：禁用颜色
	PP_LOGGER.SetColorEnabled(false);
	PPLOG_WARNING("这条消息没有颜色");

	// 测试5：启用颜色
	PP_LOGGER.SetColorEnabled(true);
	PPLOG_ERROR("颜色已恢复");

	return 0;
}

